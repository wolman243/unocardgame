    private void useCardButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_useCardButtonActionPerformed
        
        ChoosingColor chooseColor = new ChoosingColor(this);
        throwedColor = chooseColor.choseColor(handOfPlayer.get(ch));
        
       if(throwedColor != null){
        try{
            game.submitPlayerCard(game.getCurrentPlayer(), handOfPlayer.get(ch),throwedColor);
        }
        catch(InvalidColorSubmissionException ex){
            Logger.getLogger(Window.class.getName()).log(Level.SEVERE,null,ex);
        }     
        catch(InvalidPlayerTurnException ex){
            Logger.getLogger(Window.class.getName()).log(Level.SEVERE,null,ex);
        }
            
        catch(InvalidValueSubmissionException ex){
            Logger.getLogger(Window.class.getName()).log(Level.SEVERE,null,ex);
        }
     
        this.revalidate();
        
      }
       if(throwedColor != Card.Renk.Siyah)
        {
        gamephase.setName(game.getCurrentPlayer());
        gamephase.setIconsofButtons();
        cardButtonOnTop.setIcon(new javax.swing.ImageIcon("C:\\Users\\aemre\\OneDrive\\Masa端st端\\images\\PNGs\\small\\"+game.cardOnTopImage()));
        this.dispose();
        
    }//GEN-LAST:event_useCardButtonActionPerformed
    }
